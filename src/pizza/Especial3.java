package pizza;

/**
 * Clase que simula una pizza especial3
 */
public class Especial3 extends Pizza{
    
    /**
    * Constructor de una pizza especial3
    */
    public Especial3(){
        this.queso = "Cheddar";
        this.carne = "Pollo";
        this.masa = "Gruesa";
        this.precio = 220.0;
    }

}
